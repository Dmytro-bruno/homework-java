package lesson30stack;

/**
 * Інтерфейс Stackable — описує базові операції для структури стек
 * @param <E> тип елементів, що зберігаються у стеку
 */
public interface Stackable<E> {

    /**
     * Перевіряє, чи стек порожній
     * @return true якщо стек порожній
     */
    boolean isEmpty();

    /**
     * Перевіряє, чи стек повний
     * @return true якщо стек повний
     */
    boolean isFull();

    /**
     * Додає елемент до вершини стеку
     * @param item елемент, який додається
     */
    void push(E item);

    /**
     * Видаляє та повертає верхній елемент стеку
     * @return елемент з вершини стеку
     */
    E pop();

    /**
     * Повертає верхній елемент стеку, не видаляючи його
     * @return елемент з вершини стеку
     */
    E peek();
}
